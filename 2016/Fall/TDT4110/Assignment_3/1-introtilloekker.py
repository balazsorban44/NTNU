print ("From 1 to 5 with for loop")
for i in range (1,6):
    print (i)

print ("From 1 to 5 with while loop")
i=1
while i<6:
    print (i)
    i+=1

print ("From 15 to 1 with for loop")
for i in range(15,0,-1):
    print (i)

print ("From 15 to 1 with while loop")
i=15
while i>0:
    print (i)
    i-=1
